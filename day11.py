# Dan Rented two movies to watch last night.
# The first was 100 minutes long, the second 110 minutes long.
# How many hours did it take for Dan to watch the two movies
firstMovieDurationInMinutes = 100
secondMovieDurationInMinutes = 110
minutesToHour = 60
totalDuration = (float)(firstMovieDurationInMinutes +
                        secondMovieDurationInMinutes)/minutesToHour
print(f"Dan Took {totalDuration} hours to watch two movies")
